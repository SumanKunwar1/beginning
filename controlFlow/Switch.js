const month=3
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("June");
        break;

    default:
        console.log("any other rest of the month");
        break;
}